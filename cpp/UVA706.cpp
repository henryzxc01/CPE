#include <iostream>
#include <string>
using namespace std;

int main() {
    // 實戰推薦寫法：用字串陣列取代 3D 陣列，好打又不容易漏括號
    // 第一維是數字 0~9，第二維是 5 個部位 (0=上橫, 1=上直, 2=中橫, 3=下直, 4=下橫)
    // "010" 代表：左無、中有、右無 (橫線專用)
    // "101" 代表：左有、中無、右有 (直線專用)
    string g[10][5] = {
        {"010", "101", "000", "101", "010"}, // 0
        {"000", "001", "000", "001", "000"}, // 1
        {"010", "001", "010", "100", "010"}, // 2
        {"010", "001", "010", "001", "010"}, // 3
        {"000", "101", "010", "001", "000"}, // 4
        {"010", "100", "010", "001", "010"}, // 5
        {"010", "100", "010", "101", "010"}, // 6
        {"010", "001", "000", "001", "000"}, // 7
        {"010", "101", "010", "101", "010"}, // 8
        {"010", "101", "010", "001", "010"}  // 9
    };

    int s;
    string n;
    
    while (cin >> s >> n && s != 0) {
        // i 對應 5 個部位
        for (int i = 0; i < 5; i++) {
            // 判斷這個部位要印幾列：橫線(偶數)印 1 列，直線(奇數)印 s 列
            int rows = (i % 2 == 0) ? 1 : s;

            for (int r = 0; r < rows; r++) {
                // 走訪要印出的每一個數字
                for (int j = 0; j < n.size(); j++) {
                    int d = n[j] - '0'; // 將字元轉成數字 0~9

                    if (i % 2 == 0) {
                        // 處理橫向部位 (i = 0, 2, 4)
                        cout << " "; 
                        for (int k = 0; k < s; k++) cout << (g[d][i][1] == '1' ? "-" : " ");
                        cout << " "; 
                    } else {
                        // 處理直向部位 (i = 1, 3)
                        cout << (g[d][i][0] == '1' ? "|" : " "); 
                        for (int k = 0; k < s; k++) cout << " "; 
                        cout << (g[d][i][2] == '1' ? "|" : " "); 
                    }

                    // 關鍵修正：確保最後一個數字的右邊「不會」多印空白
                    if (j < n.size() - 1) cout << " ";
                }
                cout << "\n"; // 每畫完一列就換行
            }
        }
        cout << "\n"; // UVA 規定：每組測資輸出完畢後，必須補一個空白行
    }
    return 0;
}
